-- RLS POLICIES (allow all access via service_role key from backend)
-- These policies allow the backend (using the anon key + RLS bypass 
-- via service_role or with proper policies) to operate.
-- For a backend-only pattern, you can use the service_role key which 
-- bypasses RLS, or add permissive policies below.
-- ============================================================

-- Allow full access for authenticated backend operations
-- (adjust these policies based on your security requirements)
-- Using DROP IF EXISTS + CREATE pattern since PostgreSQL 
-- does not support CREATE POLICY IF NOT EXISTS.

DROP POLICY IF EXISTS "Allow all for users" ON users;
